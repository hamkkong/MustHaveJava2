package replit;

public class Exam02 {

    public static void main(String[] args) {
	// 계산 결과 출력하는 예제
	System.out.println(2 + 3);

	// 변수, 상수, 자료(데이터)형, 대입
	// 남자 주번 = "홍길동"
	// 변수들은 메모리에 기록이 됩니다.
	int num1 = 2;
	int num2 = 3;
	// 계산 결과를 변수에 대입
	int result = num1 + num2;

	// 변수에 저장된 값을 출력
	System.out.println(num1);
	System.out.println(num2);
	System.out.println(result);

    }

}
