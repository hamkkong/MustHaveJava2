package boxing;

public class Number2 {

    public static void main(String[] args) {
	
	Integer num = 7;
	
	System.out.println(num.intValue());  // 7

	System.out.println(num.longValue());  // 7
	
	System.out.println(num.floatValue());  // 7.0
	
	System.out.println(num.doubleValue());  // 7.0

    }

}
